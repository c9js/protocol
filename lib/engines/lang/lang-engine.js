/*▄─────────────────────────▄
  █                         █
  █  Создает движок языков  █
  █                         █
  ▀─────────────────────────▀*/
module.exports = class LangEngine {
/*┌─────────────┐
  │ Конструктор │
  └─────────────┘*/
    constructor(engine) {
    // Инициализируем движок
        engine(this);
        
    // Инициализируем список логических единиц
        engine.logic(
            'activator', // Логика активации выбранного языка
              'factory', // Логика инициализации списка локализаторов
        );
        
    // Активируем выбранный язык
        this.activator.activateLang();
    }
    
/*┌──────────────────────────────────┐
  │ Возвращает локализатор контракта │
  └──────────────────────────────────┘*/
    getLocalizer = (roles, role, contract) => {
        return this.factory.getLocalizer(roles, role, contract);
    }
};
