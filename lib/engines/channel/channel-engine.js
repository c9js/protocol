/*▄────────────────────────────────▄
  █                                █
  █  Создает движок каналов связи  █
  █                                █
  ▀────────────────────────────────▀*/
module.exports = class ChannelEngine {
/*┌─────────────────────────────────────┐
  │ Список поддерживаемых каналов связи │
  └─────────────────────────────────────┘*/
    channels = [
        'l2raw', // Канал связи на уровне L2
    ]
    
/*┌───────────────────────────┐
  │ Список ролей канала связи │
  └───────────────────────────┘*/
    roles = [
           'init', // Роль инициализации канала связи
        'scanner', // Роль адаптации приемника
         'sender', // Роль адаптации передатчика
    ]
    
/*┌─────────────┐
  │ Конструктор │
  └─────────────┘*/
    constructor(engine) {
    // Инициализируем движок
        engine(this);
        
    // Инициализируем список логических единиц
        engine.logic(
             'loader', // Логика загрузки выбранного канала связи
            'factory', // Логика инициализации выбранного канала связи
        );
        
    // Загружаем выбранный канал связи
        this.loader.loadChannel(engine);
    }
    
/*┌──────────────────────────────────────┐
  │ Инициализирует выбранный канал связи │
  └──────────────────────────────────────┘*/
    initChannel = () => {
        this.factory.initChannel();
    }
};
